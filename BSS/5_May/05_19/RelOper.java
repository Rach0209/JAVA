// 관계연산자
// boolean 값으로 표현된다. t/f
public class RelOper {
	public static void main(String[] args) {
		System.out.println(55 == 55); // t
		System.out.println(55 != 55); // f // != 다르냐!!?
		System.out.println(55 > 55); // f
		System.out.println(55 < 55); // f
		System.out.println(55 >= 55); // t
		System.out.println(55 <= 55); // t
	}
}