// 논리값 나타낼 때 boolean [불련]
// 확인, 논리적연산을 통해 프로그램 흐름을 제어 할 때 사용.
public class BooleanType {
	public static void main(String[] args) {
		// java에서는 무조건 true, false - 다른 언어에서는 1, 0 으로 표기하기도 함
		boolean t = true;
		boolean f = false;
		
		// 산술 연산은 불가능하다;
		System.out.println(t);
		System.out.println(f);
	}
}